package fpt.fu.dn.fpthospitalcare.fhc.controller.Medicine.Model;

public class MedicineImportModel {
	private String medicineId;
	private String medicineName;
	private String medicineIngredients;
	private String medicineUses;
	private String medicineImg;
	private String medicineProducer;
	private String userId;
	
	public String getMedicineId() {
		return medicineId;
	}
	public void setMedicineId(String medicineId) {
		this.medicineId = medicineId;
	}
	public String getMedicineName() {
		return medicineName;
	}
	public void setMedicineName(String medicineName) {
		this.medicineName = medicineName;
	}
	public String getMedicineIngredients() {
		return medicineIngredients;
	}
	public void setMedicineIngredients(String medicineIngredients) {
		this.medicineIngredients = medicineIngredients;
	}
	public String getMedicineUses() {
		return medicineUses;
	}
	public void setMedicineUses(String medicineUses) {
		this.medicineUses = medicineUses;
	}
	public String getMedicineImg() {
		return medicineImg;
	}
	public void setMedicineImg(String medicineImg) {
		this.medicineImg = medicineImg;
	}
	public String getMedicineProducer() {
		return medicineProducer;
	}
	public void setMedicineProducer(String medicineProducer) {
		this.medicineProducer = medicineProducer;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	
}
